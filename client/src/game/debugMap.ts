import type { GameMap, Position } from "./types";

const DEBUG_MAP_ROWS = [
  "########################",
  "#......#.....#.........#",
  "#......#.....#.........#",
  "#......#.....#.........#",
  "#......###.###....#....#",
  "#.................#....#",
  "####.######.########.###",
  "#....#......#......#...#",
  "#....#......#......#...#",
  "#....###.####......#...#",
  "#......................#",
  "#....#######.######.####",
  "#....#.....#......#....#",
  "#....#.....#......#....#",
  "#.####.....####.###....#",
  "#......................#",
  "#......#.....#.........#",
  "########################",
];

export function createDebugMap(): GameMap {
  const walls: Position[] = [];

  DEBUG_MAP_ROWS.forEach((row, y) => {
    row.split("").forEach((tile, x) => {
      if (tile === "#") {
        walls.push({ x, y });
      }
    });
  });

  return {
    columns: DEBUG_MAP_ROWS[0].length,
    rows: DEBUG_MAP_ROWS.length,
    walls,
  };
}
